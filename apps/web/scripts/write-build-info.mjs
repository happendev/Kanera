import { readFile, mkdir, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(scriptDir, "..");
const repoRoot = resolve(projectRoot, "../..");
const outputPath = resolve(projectRoot, "src/build-info.generated.ts");
const builtAt = new Date().toISOString();
const rootPackageJson = JSON.parse(await readFile(resolve(repoRoot, "package.json"), "utf8"));
const version = process.env.KANERA_VERSION ?? rootPackageJson.version;

const contents = `// This file is generated by scripts/write-build-info.mjs during builds.
// It is intentionally ignored by version control.
export const buildInfo = {
  version: "${version}",
  builtAt: "${builtAt}",
} as const;
`;

await mkdir(dirname(outputPath), { recursive: true });
await writeFile(outputPath, contents);
