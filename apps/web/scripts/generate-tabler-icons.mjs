import { readFile, mkdir, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { createRequire } from "node:module";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(scriptDir, "..");
const require = createRequire(import.meta.url);

// Read the installed Tabler webfont CSS so the picker list always matches the bundled icon set.
const cssPath = require.resolve("@tabler/icons-webfont/dist/tabler-icons.css");
const outputPath = resolve(projectRoot, "../../packages/shared/src/tabler-icons.ts");
const webOutputPath = resolve(projectRoot, "src/app/shared/tabler-icons.ts");
const css = await readFile(cssPath, "utf8");
const iconNames = [...css.matchAll(/\.ti-([a-z0-9-]+):before/g)]
  .map((match) => match[1])
  .filter((name) => name !== "ti")
  .sort((a, b) => a.localeCompare(b));

const uniqueIconNames = [...new Set(iconNames)];
// Keep a compact, stable layout so generated diffs stay readable when Tabler updates.
const formattedIcons = uniqueIconNames
  .reduce((lines, name, index) => {
    const lineIndex = Math.floor(index / 5);
    lines[lineIndex] ??= [];
    lines[lineIndex].push(`'${name}'`);
    return lines;
  }, [])
  .map((line) => `  ${line.join(", ")},`)
  .join("\n");

const contents = `// This file is generated by scripts/generate-tabler-icons.mjs.
// Do not edit it by hand.
export const TABLER_ICONS: string[] = [
${formattedIcons}
];
`;

await mkdir(dirname(outputPath), { recursive: true });
await writeFile(outputPath, contents);
await writeFile(webOutputPath, `// This file is generated by scripts/generate-tabler-icons.mjs.\n// Do not edit it by hand.\nexport { TABLER_ICONS } from "@kanera/shared/icons";\n`);

console.log(`Generated ${uniqueIconNames.length.toLocaleString()} Tabler icon names.`);
